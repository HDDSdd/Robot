package log;

/**
 * Уровни важности сообщений журнала логирования.
 * <p>
 * Каждый уровень имеет числовой приоритет ({@code level}), где большее значение
 * соответствует более высокой степени важности. Это позволяет фильтровать сообщения
 * по порогу: например, при уровне фильтрации {@code Warning} будут отображаться
 * только {@code Warning}, {@code Error} и {@code Fatal}.
 *
 * @see LogEntry
 * @see LogWindowSource
 * @see Logger
 */
public enum LogLevel
{
    /**
     * Трассировочный уровень: максимальная детализация.
     * <p>
     * Используется для пошаговой отладки: логирование входа/выхода из методов,
     * значений локальных переменных, промежуточных вычислений.
     * Обычно отключается в продакшене из-за высокого объёма данных.
     */
    Trace(0),

    /**
     * Отладочный уровень: информация для разработчиков.
     * <p>
     * Сообщения, полезные при диагностике поведения приложения в тестовой среде.
     * Не должны содержать конфиденциальных данных или избыточной детализации.
     */
    Debug(1),

    /**
     * Информационный уровень: штатные события приложения.
     * <p>
     * Фиксация значимых этапов работы: запуск, завершение, успешное подключение
     * к ресурсам, завершение пользовательских операций. Рекомендуется для
     * мониторинга в продакшене.
     */
    Info(2),

    /**
     * Уровень предупреждений: потенциальные проблемы.
     * <p>
     * Ситуации, которые не нарушают работу приложения, но могут привести
     * к ошибкам в будущем: устаревшие настройки, медленные запросы,
     * неоптимальные конфигурации.
     */
    Warning(3),

    /**
     * Уровень ошибок: сбои в выполнении операций.
     * <p>
     * Исключения, неудачные попытки подключения, валидационные ошибки.
     * Требуют анализа разработчиком, но не обязательно останавливают приложение.
     */
    Error(4),

    /**
     * Критический уровень: фатальные ошибки.
     * <p>
     * Ситуации, после которых приложение не может продолжать работу:
     * потеря соединения с критичной БД, нехватка памяти, повреждение данных.
     * Обычно сопровождается аварийным завершением.
     */
    Fatal(5);

    private final int level;

    /**
     * Конструирует уровень логирования с заданным числовым приоритетом.
     * <p>
     * Используется только при объявлении констант перечисления.
     * Приоритеты должны быть уникальны и возрастать по степени важности.
     *
     * @param iLevel числовой приоритет уровня (большее = более важно)
     */
    LogLevel(int iLevel)
    {
        level = iLevel;
    }

    /**
     * Возвращает числовой приоритет данного уровня логирования.
     * <p>
     * Может использоваться для сравнения уровней и фильтрации сообщений:
     * <pre>{@code
     * if (entry.getLevel().level() >= LogLevel.Warning.level()) {
     *     // Обработать только предупреждения и выше
     * }
     * }</pre>
     *
     * @return целочисленный приоритет уровня ({@code 0} для {@link #Trace},
     *         {@code 5} для {@link #Fatal})
     */
    public int level()
    {
        return level;
    }
}